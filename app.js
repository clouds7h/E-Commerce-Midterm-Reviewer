// ------- state -------
const quizEl = document.getElementById("quiz");
const searchEl = document.getElementById("search");
const resetBtn = document.getElementById("resetBtn");

const statAnswered = document.getElementById("statAnswered");
const statTotal = document.getElementById("statTotal");
const statCorrect = document.getElementById("statCorrect");

statTotal.textContent = QUESTIONS.length;

function escapeHtml(str){
  return str.replace(/[&<>"']/g, m => ({
    "&":"&amp;","<":"&lt;",">":"&gt;",'"':"&quot;","'":"&#039;"
  }[m]));
}

function keyFor(i){ return `ans_${i}`; }
function getSaved(i){
  const v = sessionStorage.getItem(keyFor(i));
  return v === null ? null : Number(v);
}
function setSaved(i, choice){
  sessionStorage.setItem(keyFor(i), String(choice));
}
function clearSaved(){
  QUESTIONS.forEach((_, i) => sessionStorage.removeItem(keyFor(i)));
}

function computeStats(){
  let answered = 0, correct = 0;
  QUESTIONS.forEach((q, i) => {
    const sel = getSaved(i);
    if(sel !== null && !Number.isNaN(sel)){
      answered++;
      if(sel === q.answer) correct++;
    }
  });
  statAnswered.textContent = answered;
  statCorrect.textContent = correct;
}

function render(){
  const filter = (searchEl.value || "").trim().toLowerCase();
  quizEl.innerHTML = "";

  QUESTIONS.forEach((item, idx) => {
    const match = !filter ||
      item.q.toLowerCase().includes(filter) ||
      item.choices.some(c => c.toLowerCase().includes(filter));

    if(!match) return;

    const card = document.createElement("div");
    card.className = "card";
    card.dataset.index = idx;

    const selected = getSaved(idx);

    const head = document.createElement("div");
    head.className = "cardhead";
    head.innerHTML = `
      <p class="qtitle"><b>Q${idx+1}.</b> ${escapeHtml(item.q)}</p>
      <div class="badge" id="badge_${idx}"></div>
    `;

    const choices = document.createElement("div");
    choices.className = "choices";

    item.choices.forEach((c, cIdx) => {
      const lab = document.createElement("label");
      lab.className = "choice";
      lab.innerHTML = `
        <input type="radio" name="q_${idx}" value="${cIdx}" ${selected === cIdx ? "checked" : ""}/>
        <span>${escapeHtml(c)}</span>
      `;
      choices.appendChild(lab);
    });

    const fb = document.createElement("div");
    fb.className = "feedback hidden";
    fb.id = `fb_${idx}`;

    card.appendChild(head);
    card.appendChild(choices);
    card.appendChild(fb);

    quizEl.appendChild(card);

    if(selected !== null && !Number.isNaN(selected)){
      showFeedback(idx, selected);
    }
  });

  computeStats();
}

function showFeedback(qIndex, selected){
  const item = QUESTIONS[qIndex];
  const fb = document.getElementById(`fb_${qIndex}`);
  const badge = document.getElementById(`badge_${qIndex}`);

  fb.classList.remove("hidden");
  fb.classList.remove("good","bad");

  if(selected === item.answer){
    fb.classList.add("good");
    fb.innerHTML = `✅ Correct!`;
    badge.textContent = "Correct";
    badge.style.color = "var(--good)";
  } else {
    fb.classList.add("bad");
    fb.innerHTML = `❌ Wrong. Correct answer: <b>${escapeHtml(item.choices[item.answer])}</b>`;
    badge.textContent = "Wrong";
    badge.style.color = "var(--bad)";
  }
}

quizEl.addEventListener("change", (e) => {
  const t = e.target;
  if(!t || !t.matches('input[type="radio"]')) return;

  const card = t.closest(".card");
  const qIndex = Number(card.dataset.index);
  const choiceIndex = Number(t.value);

  setSaved(qIndex, choiceIndex);
  showFeedback(qIndex, choiceIndex);
  computeStats();
});

searchEl.addEventListener("input", render);

resetBtn.addEventListener("click", () => {
  clearSaved();
  render();
});

render();
